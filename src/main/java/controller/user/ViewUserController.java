package controller.user;

import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import controller.Controller;
import model.Profile;
import model.service.StudentManager;
import model.service.StudentNotFoundException;
import model.Student;

public class ViewUserController implements Controller {
    @Override
    public String execute(HttpServletRequest request, HttpServletResponse response) throws Exception {			
    	// 로그인 여부 확인
    	if (!UserSessionUtils.hasLogined(request.getSession())) {
            return "redirect:/student/login";		// login form 요청으로 redirect
        }

		Profile profile = null;

		ProfileManager manager = ProfileManager.getInstance();
		String studentId = request.getParameter("studentId");

    	try {
			profile = manager.findStudent(studentId);	// 사용자 정보 검색
		} catch (StudentNotFoundException e) {
	        return "redirect:/student/main";
		}	
		
    	request.setAttribute("student", student);		// 사용자 정보 저장
		return "/profile/view.jsp";				// 사용자 보기 화면으로 이동
    }
}
